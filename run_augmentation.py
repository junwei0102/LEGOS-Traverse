# CLI helpers
# ------------------------------


TIME_REGEX = re.compile(r"\b([01]?\d|2[0-3]):([0-5]\d)(?::([0-5]\d))?\b")


def _parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Augment a trace using extracted measure domain cues.")
    parser.add_argument("input", type=Path, help="Path to the input trace file.")
    parser.add_argument(
        "--measure-domain",
        type=Path,
        required=True,
        help=(
            "JSON array output from extract_context.py "
            "(system_agent/interacting_agent/user/location/time). Time entries provide scenario start CLOCK."
        ),
    )
    parser.add_argument(
        "--output-dir",
        type=Path,
        default=Path("."),
        help="Directory to store augmentation artefacts.",
    )
    parser.add_argument(
        "--api-key",
        help="API key for the language model (optional; falls back to OPENAI_API_KEY env var).",
    )
    parser.add_argument("--model", default="gpt-5.1-2025-11-13", help="OpenAI model name (e.g., gpt-5.1-2025-11-13).")
    parser.add_argument("--temperature", type=float, default=0.2, help="Sampling temperature.")
    return parser.parse_args()


def _load_measure_domain(path: Path) -> list:
    try:
        data = json.loads(path.read_text(encoding="utf-8"))
    except Exception as exc:  # noqa: BLE001
        raise SystemExit(f"Failed to read measure-domain file {path}: {exc}") from exc

    if not isinstance(data, list):
        raise SystemExit(f"Measure-domain file {path} must contain a JSON array.")
    return data


def _derive_start_clocks(domain_measures: list) -> List[str]:
    clocks: List[str] = []
    for entry in domain_measures:
        if entry.get("category") != "time":
            continue
        text = f"{entry.get('description', '')} {entry.get('term', '')}"
        for match in TIME_REGEX.finditer(text):
            value = match.group(0)
            if value not in clocks:
                clocks.append(value)
    if not clocks:
        raise SystemExit("Measure-domain file must include at least one time entry with HH:MM for scenario start.")
    # Sort by HH:MM numeric for stability
    def _to_minutes(t: str) -> int:
        parts = t.split(":")
        return int(parts[0]) * 60 + int(parts[1])

    clocks.sort(key=_to_minutes)
    return clocks


TRACE_LINE_PATTERN = re.compile(r"^\s*at time\s+(\d+):\s*(.+)$", re.IGNORECASE)


def _parse_trace_lines(trace_text: str) -> List[tuple]:
    lines: List[tuple] = []
    for raw in trace_text.splitlines():
        match = TRACE_LINE_PATTERN.match(raw)
        if not match:
            continue
        time_val = int(match.group(1))
        content = match.group(2).strip()
        is_measure = content.lower().startswith("measure")
        lines.append((time_val, content, is_measure))
    return lines


def main() -> None:
    args = _parse_args()

    domain_measures = _load_measure_domain(args.measure_domain)
    start_clocks = _derive_start_clocks(domain_measures)
    config = AugmentationConfig(domain_measures=domain_measures, start_clocks=start_clocks)

    trace_text = load_input(args.input)
    trace_lines = _parse_trace_lines(trace_text)
    if not trace_lines:
        raise SystemExit("Input trace must contain lines like 'at time X: <event>'.")
    prompt = build_prompt(trace_text, config)

    response = call_llm(
        args.api_key,
        prompt,
        model=args.model,
        temperature=args.temperature,
    )

    artifacts = parse_augmented_response(response)
    saved = save_artifacts(
        artifacts=artifacts,
        output_dir=args.output_dir,
        trace_lines=trace_lines,
        start_clocks=start_clocks,
        domain_measures=domain_measures,
    )

    print("Augmentation complete. Artefacts written to:")
    for label, path in saved.items():
        print(f"- {label}: {path}")


if __name__ == "__main__":
    main()
